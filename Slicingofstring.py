#                   ****SLICING of Strings******
#                        (mean a piece).

#[]operator is called (SLICE OPERATOR)-used to retrieve parts of STRING

#PYTHON STRINGS follows(Zero Based Index)
#Index can be (+ve) or(-ve).
#(+ve)-means forward direction from(LEFT TO RIGHT)
#(-ve)-means backward direction from(RIGHT TO LEFT)
#______________________________________________________
s="durga"
print(s[0])
print(s[1])
print(s[2])
print(s[-1])
print(s[1:40])
print(s[1:])
print(s[:])
print(s*3)
print(len(s))
