#(NOTE):- In string Literals we can use escape characters to associate a special meaning.

s="shivansh\nGupta" #(NEW LINE--> "\n")
print(s)
print()

d="&emicolon\tMeraNaam" #(Horizontal Tab--> "\t")
print(d)
print()

s1="Hello\rKaiseho" #(Carriage Return-->"\r")
print(s1)
print()

s2="shiva\bgupta"#(backspace-->"\b")
print(s2)
print()

v="Aur\fkitthhe" #(Form Feed-->"\f")
print(v)
print()

c="Lamborghini\vaaechalyejaneoo" #(Vertical Tab)
print(c)
print()

print("Sanno\\bhi\'chotta de\"doo") 
